test_that("traveling saleman works",{
  s <- c(0,0,0,1,1,2)
  t <- c(1,2,3,2,3,3)
  d <- c(1,2,3,4,5,6)
  out <- TravelingSalesman(s,t,d,4,"Christofides")
  expect_equal(out[[1]],c(2,0,1,3))
  expect_equal(out[[2]],14)
  out <- TravelingSalesman(s,t,d,4,"Greedy")
  expect_equal(out[[1]],c(0,1,3,2))
  expect_equal(out[[2]],14)
  out <- TravelingSalesman(s,t,d,4,"Insertion")
  expect_equal(out[[1]],c(0,1,2,3))
  expect_equal(out[[2]],14)
  out <- TravelingSalesman(s,t,d,4,"NearestNeighbor")
  expect_equal(out[[1]],c(2,0,1,3))
  expect_equal(out[[2]],14) 
  out <- TravelingSalesman(s,t,d,4,"Opt2")  
  expect_equal(out[[1]],c(0,1,2,3))
  expect_equal(out[[2]],14)
  expect_error(TravelingSalesman(s,t,d,4,"abc"), "Invalid")  
})
